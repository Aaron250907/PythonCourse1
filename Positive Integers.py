Number1 = int(input("Enter Any Number : "))

if Number1 >0 :
    print(Number1, "is a positive Integer")
    print("Square of ",Number1, "is = ", Number1*Number1)

elif Number1 <0 :
    print(Number1, "is a negative Integer")
    print("Modulus of",Number1, "is =",0-Number1)

else:
    print(Number1, "is neither Positive nor negative")
